
# This file contain how you can change a value of a string from anycase to swapcase case or upper case


"Animals".swapcase()  # normal way this will be not print in normal terminal you may need python shell


food = "Badger"  # just assign a string with value to the variable
print (food.swapcase())  # you know just print

bee = 'Honey Bee'.swapcase()  # assign a string and add a funtion to print swapcase case with value to the variable
print (bee)  # just prints

warm="Honey Badger"  # string to the variable
warm_funtion=warm.swapcase()  # just try something new
print (warm_funtion)  # just print



new_try = "apple" " " "banana" ' ' 'mango'  # no matter how many words you type it takes just a single variable
new_try_final = new_try.swapcase()  # assign a new variable to the previous variable ( this helps for less type )
print (new_try_final)  # just print


sentence = "I cAn prInT SenTenCe alSo"  # just a sentence define to a variable
sentence_2 = sentence.swapcase()  # that past variable assign to a new variable with swapcase funtion
print (sentence_2)  # just print

print_with_new_line = """I can print
Multiline with
This""".swapcase()  # just assign a new string to the variable with swapcase funtion
print (print_with_new_line)  # just print


